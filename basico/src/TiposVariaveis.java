public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        
        //tipos inteiros
        byte numeroPequeno = 1;
        short numeroCurto = 2022;
        int numeroNormal = 1009909;
        long numeroGrande = 1983727637626L;

        //decimais
        float peso = 55.7F;
        double salario = 129747788.98;

        //boolean
        boolean verdadeiro = true;
        boolean falso = false;

        //Caracter
        char letra = 'A';
        String nome = "Carlos"; //String é um objeto e não um tipo primitivo
        
    }
}
